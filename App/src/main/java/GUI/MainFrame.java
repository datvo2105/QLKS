	private void booking_btnUpdateActionPerformed(
		java.awt.event.ActionEvent evt) {
		BLL.Room roomUpdate = getInitRoom();

		if (roomSelected == null || roomUpdate == null) {
			return;
		}

		roomUpdate.setPubKey(roomSelected.getPubKey() == null ? "" : roomSelected.getPubKey());
		roomUpdate.setPriKey(roomSelected.getPriKey() == null ? "" : roomSelected.getPriKey());

		JOptionPane.showMessageDialog(null, Room.updateRoom(roomUpdate) ? "Update success" : "Update fail!");
		this.initData();
	}

